/**
 * sub - Returns the subtracts of two numbers
 * @a: first number
 * @b: second number
 *
 * Return: The difference of a - b.
 */
int sub(int a, int b)
{
	return (a - b);
}

/**
 * add - the sum of two numbers
 * @a: first number
 * @b: second number
 *
 * Return: The sum of (a and b).
 */
int add(int a, int b)
{
	return (a + b);
}

/**
 * div - the division of two numbers.
 * @a: first number
 * @b: second number
 *
 * Return: The quotient of a / b.
 */
int div(int a, int b)
{
	return (a / b);
}

/**
 * mul - Multiply of two numbers.
 * @a: first number.
 * @b: second number.
 *
 * Return: The product of a and b.
 */
int mul(int a, int b)
{
	return (a * b);
}

/**
 * mod - the remainder of the division of two numbers
 * @a: first number
 * @b: second number
 *
 * Return: The remainder of the division of a by b.
 */
int mod(int a, int b)
{
	return (a % b);
}
